/* ---------- 内部函数：显示单个字符 ---------- */
static void OLED_ShowChar(uint8_t x, uint8_t y, char ch)
{
    uint8_t i;
    if (ch < ' ' || ch > '~') return;

    uint8_t idx = ch - ' ';

    OLED_WriteCmd(0xB0 + y);                          // 页地址
    OLED_WriteCmd(((x & 0xF0) >> 4) | 0x10);           // 列高4位
    OLED_WriteCmd(x & 0x0F);                           // 列低4位

    for (i = 0; i < 8; i++)
    {
        OLED_WriteData(Font6x8[idx][i]);
    }
}

/* ---------- 初始化 ---------- */
void OLED_Init(void)
{
    GPIO_InitTypeDef g;

    RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB, ENABLE);

    g.GPIO_Pin   = OLED_SCL_PIN | OLED_SDA_PIN;
    g.GPIO_Mode  = GPIO_Mode_Out_PP;
    g.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_Init(GPIOB, &g);
    SCL_H();
    SDA_H();

    __disable_irq();

    delay_ms(100);

    OLED_WriteCmd(0xAE);    // 关闭显示
    OLED_WriteCmd(0x20);    // 设置内存地址模式
    OLED_WriteCmd(0x00);    // 水平寻址模式
    OLED_WriteCmd(0xB0);    // 页起始地址
    OLED_WriteCmd(0xC8);    // COM 扫描方向
    OLED_WriteCmd(0x00);    // 列低4位
    OLED_WriteCmd(0x10);    // 列高4位（列起始=0）
    OLED_WriteCmd(0x40);    // 显示起始行
    OLED_WriteCmd(0x81);    // 对比度
    OLED_WriteCmd(0xFF);    // 最大
    OLED_WriteCmd(0xA1);    // 左右镜像
    OLED_WriteCmd(0xA6);    // 正常显示
    OLED_WriteCmd(0xA8);    // 多路复用比
    OLED_WriteCmd(0x3F);    // 1/64 duty
    OLED_WriteCmd(0xA4);    // 全局显示开启
    OLED_WriteCmd(0xD3);    // 显示偏移
    OLED_WriteCmd(0x00);
    OLED_WriteCmd(0xD5);    // 时钟分频
    OLED_WriteCmd(0xF0);
    OLED_WriteCmd(0xD9);    // 预充电周期
    OLED_WriteCmd(0x22);
    OLED_WriteCmd(0xDA);    // COM 引脚配置
    OLED_WriteCmd(0x12);
    OLED_WriteCmd(0xDB);    // VCOMH 电压
    OLED_WriteCmd(0x20);
    OLED_WriteCmd(0x8D);    // 电荷泵
    OLED_WriteCmd(0x14);    // 启用
    OLED_WriteCmd(0xAF);    // 打开显示

    __enable_irq();
}

/* ---------- 清屏 ---------- */
void OLED_Clear(void)
{
    uint8_t page, col;
    for (page = 0; page < 8; page++)
    {
        OLED_WriteCmd(0xB0 + page);
        OLED_WriteCmd(0x00);
        OLED_WriteCmd(0x10);
        for (col = 0; col < 128; col++)
            OLED_WriteData(0x00);
    }
}

/* ---------- 显示字符串 ---------- */
void OLED_ShowString(uint8_t x, uint8_t y, const char *str)
{
    while (*str)
    {
        OLED_ShowChar(x, y, *str);
        x += 6;                     // 6 像素宽度 + 2 像素间距
        if (x > 122) { x = 0; y++; } // 自动换行
        str++;
    }
}