#include "oled.h"
#include "delay.h"
#include <string.h>

#define OLED_ADDR      0x78          // 0x3C << 1

#define OLED_SCL_PORT  GPIOB
#define OLED_SCL_PIN   GPIO_Pin_10
#define OLED_SDA_PORT  GPIOB
#define OLED_SDA_PIN   GPIO_Pin_11

/* ---------- 软件 I2C 宏 ---------- */
static void SDA_OUT(void)
{
    GPIO_InitTypeDef g;
    g.GPIO_Pin   = OLED_SDA_PIN;
    g.GPIO_Mode  = GPIO_Mode_Out_PP;
    g.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_Init(OLED_SDA_PORT, &g);
}

static void SDA_IN(void)
{
    GPIO_InitTypeDef g;
    g.GPIO_Pin   = OLED_SDA_PIN;
    g.GPIO_Mode  = GPIO_Mode_IPU;
    GPIO_Init(OLED_SDA_PORT, &g);
}

#define SCL_H() GPIO_SetBits(OLED_SCL_PORT, OLED_SCL_PIN)
#define SCL_L() GPIO_ResetBits(OLED_SCL_PORT, OLED_SCL_PIN)
#define SDA_H() GPIO_SetBits(OLED_SDA_PORT, OLED_SDA_PIN)
#define SDA_L() GPIO_ResetBits(OLED_SDA_PORT, OLED_SDA_PIN)
#define SDA_R() GPIO_ReadInputDataBit(OLED_SDA_PORT, OLED_SDA_PIN)

/* ---------- 软件 I2C 基本操作 ---------- */
static void I2C_Start(void)
{
    SDA_OUT();
    SDA_H(); SCL_H();
    delay_us(5);
    SDA_L();
    delay_us(5);
    SCL_L();
}

static void I2C_Stop(void)
{
    SDA_OUT();
    SDA_L(); SCL_H();
    delay_us(5);
    SDA_H();
    delay_us(5);
}

static uint8_t I2C_WriteByte(uint8_t data)
{
    uint8_t i;
    SDA_OUT();
    for (i = 0; i < 8; i++)
    {
        if (data & 0x80)
            SDA_H();
        else
            SDA_L();
        delay_us(2);
        SCL_H();
        delay_us(2);
        SCL_L();
        data <<= 1;
    }
    SDA_IN();
    delay_us(2);
    SCL_H();
    delay_us(2);
    uint8_t ack = SDA_R();
    SCL_L();
    SDA_OUT();
    return ack;
}

/* ---------- OLED 写命令/数据（关中断保护 I2C 时序）---------- */
static void OLED_WriteCmd(uint8_t cmd)
{
    __disable_irq();
    I2C_Start();
    I2C_WriteByte(OLED_ADDR);
    I2C_WriteByte(0x00);
    I2C_WriteByte(cmd);
    I2C_Stop();
    __enable_irq();
}

static void OLED_WriteData(uint8_t data)
{
    __disable_irq();
    I2C_Start();
    I2C_WriteByte(OLED_ADDR);
    I2C_WriteByte(0x40);
    I2C_WriteByte(data);
    I2C_Stop();
    __enable_irq();
}

